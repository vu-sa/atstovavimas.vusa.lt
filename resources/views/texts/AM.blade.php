            interesus, teikti pasiūlymus dėl aukštojo mokslo biudžeto projektų rengimo ir finansavimo subsidijų
            aukštosioms mokykloms skyrimo, teikti siūlymus dėl kolegijų finansavimo ir siekti koleginio sektoriaus
            veiklos stiprinimo ir vystymo Lietuvos koleginėje aukštojo mokslo sistemoje. </p>

        <p>Gali deleguoti savo atstovus į ŠMSM (ir ne tik) sudarytas darbo grupes ar komisijas, kurios
            susijusios su aukštuoju mokslu.</p>

