    <section id="vu-kap">
        <h3>Vilniaus universiteto kamieniniai akademiniai padaliniai</h3>

        <p>Vilniaus universiteto kamieniniai akademiniai padaliniai (toliau – VU KAP) – fakultetai, institutai, centrai,
            mokyklos – tai vidiniai organizaciniai Universiteto dariniai, kuriuose vykdoma visų rūšių pagrindinė
            Universiteto veikla. KAP veiklos tikslai – rengti aukštos kvalifikacijos specialistus ir mokslininkus,
            plėtoti aukšto tarptautinio lygio mokslinius tyrimus, užtikrinti mokslo ir studijų vienovę, užtikrinti
            mokslinių tyrimų ir studijų kokybę ir kt.</p>

        <section id="vu-taryba">
            <h4>VU KAP Taryba</h4>
            <p>VU KAP Taryba – kolegialus VU KAP valdymo organas, renkamas 4 metams. VU KAP taryba renka VU KAP vadovą,
                ir tvirtina jo pavaduotojus, VU KAP veiklos planą, biudžetą, mokslinių tyrimų kryptis, SPK ir VU KAP
                komisijų sudėtis (išskyrus studentų atstovus), svarsto naujas studijų programas bei esamų studijų
                programų keitimus, vertina mokslinių tyrimų ir studijų rezultatus. VU KAP Tarybą sudaro VU KAP vadovas,
                dėstytojai, akademinės ir neakademinės bendruomenės nariai bei socialiniai partneriai ir studentai. </p>
        </section>
        <section id="vu-kap-tarybos-pirmininkas">
            <h5>VU KAP Tarybos pirmininkas</h5>
            <p>VU KAP Tarybos pirmininkas – organizuoja sklandų VU KAP Tarybos darbą. </p>
        </section>
        <section id="vu-kap-vadovas">
            <h4>VU KAP vadovas</h4>

            <p>VU KAP vadovas yra renkamas KAP Tarybos 5 metams ir atsako už vadovavimą KAP bei už KAP veiklos plano
                įgyvendinimą ir jo atitikimą Universiteto Statutui, bendrą studijų programų ir mokslinių tyrimų
                efektyvumą bei raidos kokybę pagrindinėje KAP veiklos srityje.</p>
        </section>
        <section id="vu-kap-vadovo-pavaduotojai">
            <h5>VU KAP vadovo pavaduotojai </h5>

            <p>KAP vadovas turi pavaduotojus, kurių vienas atsakingas už KAP studijų organizavimą, o kiti – už
                mokslinius tyrimus, KAP infrastruktūros naudojimą ir plėtrą. </p>
        </section>
        <section id="vu-sap">
            <h4>VU Šakinis akademinis padalinys</h4>

            <p>VU Šakinis akademinis padalinys (toliau – ŠAP) – KAP struktūrinis padalinys, kurio pagrindinė veikla –
                moksliniai tyrimai ir studijos.</p>
        </section>
        <section id="vu-sap-vadovas">
            <h5>VU ŠAP vadovas</h5>

            <p>ŠAP vadovas atsako už vadovavimą ŠAP, ŠAP veiklos plano parengimą ir įgyvendinimą vadovaujantis KAP
                veiklos planu bei už ŠAP vykdomos mokslo veiklos kokybę ir rezultatus bei sąsajas su atitinkamų krypčių
                studijomis. </p>
        </section>
        <section id="vu-kap-dekanatas">
            <h4>VU KAP dekanatas / direktoratas (jeigu KAP nėra fakultetas)</h4>

            <p>Nuolat veikianti VU KAP vadovui veiklos organizavimo klausimais patarianti institucija. Į jos sudėtį
                įeina VU KAP vadovo pavaduotojai, ŠAP vadovai ir VU SA P pirmininkas.</p>
        </section>
        <section id="studiju-kolegija">
            <h4>Studijų kolegija</h4>

            <p>Pataria už studijų organizavimą atsakingam VU KAP vadovo pavaduotojui studijų vykdymo ir jų kokybės
                gerinimo klausimais. Studijų kolegiją sudaro VU KAP veikiančių SPK pirmininkai ir VU SA deleguotas
                studentų atstovas. </p>
        </section>
        <section id="studiju-programu-komitetai">
            <h4>Studijų programų komitetai</h4>

            <p>Studijų programų komitetai (toliau – SPK) studijų programų valdymo dariniai, atsakingi už studijų
                programų kokybę bei jų siekinių įgyvendinimą.</p>

            <p>Pagrindinės SPK funkcijos:</p>

            <ol>
                <li>Stebėti ir tobulinti studijų programos tikslų, ugdomų kompetencijų, turinio, metodų ir studentų
                    pasiekimų vertinimų vientisumą, studijų programos aktualumą ir konkurencingumą;</li>

                <li>Analizuoti studijų programos įgyvendinimą, identifikuoti problemas ir siūlyti jų sprendimo būdus;
                </li>

                <li>Rengtia studijų programos realizavimo ataskaitas ir pristatyti jas VU KAP tarybai bent kartą per
                    mokslo metus;</li>

                <li>Tvirtina kreditų kaupimo ir perkėlimo sprendimus, individualius studijų planus;</li>

                <li>Tvirtina neformaliuoju ir savišvietos būdu įgytas kompetencijas.</li>
            </ol>

            <p>Į studentų atstovą savo SPK gali kreiptis, jei turi pasiūlymų, kaip būtų galima pagerinti studijų
                programos vykdymo kokybę bei kitais studijų programos vykdymo ar individualaus studijų plano sudarymo
                klausimais.</p>

            <p>Išsamiau: <a
                    href="https://www.vu.lt/site_files/Reguliaminas/SPK_nuostatai_galutinis_2018.pdf">https://www.vu.lt/site_files/Reguliaminas/SPK_nuostatai_galutinis_2018.pdf</a>
            </p>
        </section>
