                įstatymu</a></p>
        <p>Vyriausybė prie aukštojo mokslo politikos įgyvendinimo prisideda dvejopai. Pirma, ji pateikia išvadas Seimo
            komitetams dėl teikiamų įstatymų projektų bei teikia paaiškinimus Seimo nariams aktualiais ir svarstomais
            klausimais, ar pati inicijuoja įstatymų pakeitimus.</p>
        <p>Antra, tam tikri sprendimai, gali būti priimami Vyriausybės nutarimais, todėl lygiai taip pat, kaip ir Seimo
            atveju, svarbu žinoti kaip organizuojamas Vyriausybės darbas bei kaip prie to prisideda Ministras
            Pirmininkas, Vyriausybės kancleris ir kiekviena ministerija.</p>
        <p>Kadangi studentų atstovai gali kreiptis tiesiogiai į Vyriausybę dėl Vyriausybės nutarimų iniciavimo ar
            pakeitimo, todėl schemoje prie atitinkamų ministerijų yra nurodyti su studentais susiję tų ministerijų
            kuruojami klausimai. </p>
