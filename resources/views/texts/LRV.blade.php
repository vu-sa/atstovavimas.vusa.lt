        <h3>Vyriausybė</h3>

        <p>Vyriausybė – tai Lietuvos Respublikos įstatymų vykdomoji valdžia. Vyriausybė sudaro 14 ministerijų ministrai
            ir ministras pirmininkas, Seimo paskirti ketverių metų kadencijai.</p>
        <p>Vyriausybė koordinuoja ministerijų ir kitų Vyriausybės įstaigų veiklą, vykdo Seimo priimtus įstatymus ir
            siūlo įstatymų projektus, sudaro valstybės biudžeto projektą, Seimui jį patvirtinus, užtikrina sklandų jo
            įgyvendinimą. Vykdo kitas pareigas, kurias Vyriausybei paveda LR Konstitucija ir kiti įstatymai.</p>
        <p>Vyriausybės įtaka aukštojo mokslo politikos procesuose sprendžiamoji ir vykdomoji: registruojami su aukštojo
            mokslo politikos procesais susiję įstatymų projektai, kurie pateikiami Seimui, Vyriausybės posėdžiuose
            priimami sprendimai pagal kompetenciją jai priskirtų aukštojo mokslo klausimų.</p>
        <p>Išsamiau apie Vyriausybės veiklą:</p>
        <ul>
            <li><a target="_blank" href="https://lrv.lt/lt/aktuali-informacija/apie-vyriausybe/vyriausybe">Apie Vyriausybę</a></li>
            <li><a target="_blank" href="https://lrv.lt/lt/aktuali-informacija/apie-vyriausybe/ministrai">Ministrai</a></li>
        </ul>
    </section>

