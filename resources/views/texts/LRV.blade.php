    <section id="lrv-komitetai-komisijos">
        <h3>Vyriausybės komitetai ir komisijos</h3>
        <p>Vyriausybės komitetai yra Vyriausybės patariamosios institucijos. Jų tikslas – teikti pasiūlymus dėl jos
            veiklos prioritetų, politikos ir strategijos tam tikroje srityje pasirinkimo, prireikus derinti ministrų
            pozicijas sprendžiant valstybės valdymo reikalus.</p>
        <p>Vyriausybės komisijos sudaromos Vyriausybės pavestoms užduotims atlikti. Vyriausybės komisijos sudaromos iš
            valstybės ir savivaldybių institucijų ir įstaigų, asociacijų, mokslo ir studijų institucijų atstovų,
            nepriklausomų ekspertų.</p>
        <p>Su aukštuoju mokslu susijusi komisija, veikianti Vyriausybėje yra Mokslo, technologijų ir inovacijų taryba
            (STRATA)</p>

        <p>Išsamiau galite susipažinti <a target="_blank" href="https://lrv.lt/lt/apie-vyriausybe/komitetai-ir-komisijos">čia</a></p>
    </section>

    <section id="lrv-ministerijos">
        <h3>Lietuvos Respublikos ministerijos</h3>
        <p>Ministerija steigiama formuoti valstybės politiką ir koordinuoti jos įgyvendinimą ministrui pavestose
            srityse. Ministras vadovauja ministerijai, sprendžia ministerijos kompetencijai priklausančius klausimus.
        </p>
        <p>Išsamiau galite susipažinti <a target="_blank" href="https://lrv.lt/lt/ministerijos">čia</a></p>

        <section id="aplinkos-ministerija">
            <h4>Aplinkos ministerija</h3>
                <p>Formuoja ir prižiūri klimato kaitos valdymo politiką, susijusią švietėjišką veiklą ir reguliavimą.
                    Administruoja ir finansuoja klimato kaitos programos projektus. </p>
                <p>Prižiūri Jungtinių tautų darbotvarkę darnaus vystymosi tikslams, formuoja šios darbotvarkės
                    įgyvendinimą Lietuvoje ir strategiją.</p>
                <p>
                    Rengia darnaus vystymosi tikslais remiamus reikalavimus ir rekomendacijas miestų (urbanistinei)
                    plėtrai. (aktualu akademiniams miesteliams ir jų darniai plėtrai)
                    Savo rekomendacijomis ir reikalavimais nustato kokybinius kriterijus naujų pastatų (bendrabučių ar
                    kitų Univ. Pastatų) statyboms.
                </p>
                <p>Gali steigti praktikos vietas ir suteikti kitas galimybes ją atlikti studentams, kurie studijuoja
                    aplinkos apsaugos ar gamtos mokslų studijų kryptyse.</p>
                <p>Gali steigti stipendijas studentų tiksliniam skatinimui, kurie studijuoja su aplinkos apsauga
                    susijusiose studijų kryptyse.</p>
        </section>

        <section id="ekonomikos-ir-inovaciju-ministerija">
            <h4>Ekonomikos ir inovacijų ministerija</h4>
            <p>Formuoja institucijas ir projektus, nukreiptus į inovacijų ir verslo startuolių kūrimą (pvz., studentų
                startuoliai)</p>
            <p>Rūpinasi skaitmeninėmis visuomenės ir verslų kompetencijomis - vykdo programas ir formuoja valstybės
                specialistų poreikį skaitmeninėje srityje.</p>
            <p>Kuruoja „4-osios pramonės” programą ir pramonės transformavimą skaitmenizuojant jos procesus. Tai yra
                galimybė studentams taikyti specifines žinias praktikų metu ir plėtoti industrinę doktorantūrą. (ar
                priartinti bakalauro,magistro studijas prie rinkos poreikių)</p>
            <p>Gali steigti praktikos vietas studentams, kurie studijuoja su vadyba, ekonomika ar inovacijomis
                susijusiose studijų programoje.</p>
            <p>Gali suteikti tikslinį finansavimą studentų verslumo iniciatyvoms ir su vadyba, ekonomika ar inovacijomis
                susijusiose studijų programų studentams.</p>
        </section>

        <section id="energetikos-ministerija">
            <h4>Energetikos ministerija</h4>
            <p>Kuruoja klimato kaitos klausimą per atsinaujinančių energetinių išteklių plėtra. Valdo energetikos
                sektorių ir jame veikiančias valstybės įmones, kuriose studentai iš gamtos mokslų studijų krypčių
                atlieka praktikas.</p>
            <p>Gali steigti stipendijas studentų tiksliniam skatinimui, kurie studijuoja fizinėse, technologinėse ar
                inžinerinėse studijų programose ir siejasi su energetika.</p>
        </section>

        <section id="finansu-ministerija">
            <h4>Finansų ministerija</h4>
            <p>Užsiima valstybės turto valdymu per ministerijos ir jai pavaldžių įstaigų veiklas (kai kurių bendrabučių
                nuosavybė yra Finansų Ministerijos pavaldžiose institucijose).</p>
            <p>Kuruoja valstybės biudžeto asignavimus (skiriamas lėšas) švietimui ir mokslui, konsultuoja dėl tikslingo
                jų panaudojimo ir tai vertina.</p>
            <p>Vertina finansinių rodiklių naudojimą priimamuose teisės aktuose (pavyzdžiui, susiję su švietimu - kaip
                apskaičiuojamos stipendijos) ir teikia išvadas jų korektiškumui.</p>
        </section>

        <section id="krasto-apsaugos-ministerija">
            <h4>Krašto apsaugos ministerija</h4>
            <p>Formuoja karo prievolės politiką, nustato reikalavimus jos atlikimui ir šaukimui į kariuomenę.</p>
            <p>Prižiūri Lietuvos kariuomenei aktualų paramedikų rengimą (arti medicinos studijų ir studentų).</p>
            <p>Finansuoja specifines į karo studijas nukreiptas aukštąsias mokyklas (LKA)</p>
            <p>Organizuoja jaunesniųjų karininkų vadų mokymus.</p>
        </section>

        <section id="kulturos-ministerija">
            <h4>Kultūros ministerija</h4>
            <p>Remia akademinės bendruomenės vystomus kultūros projektus. Aktualu menų ir humanitarinių mokslų studijų
                studentams.</p>
            <p>Kuruoja žurnalistiką ir viešosios informacijos rengimą, nustato reikalavimus ir normas žurnalistinei ir
                viešosios informacijos rengimo veiklai. Aktualu komunikacijos mokslus studijuojantiems studentams.</p>
            <p>Kuruoja muziejų, bibliotekų ir archyvų veiklą, kurie svarbūs istorijos, filologijos, filosofijos ir kitų
                socialinių mokslų studentų praktikoms, studijų tyriamiesiems darbams.</p>
            <p>Finansuoja ir remia jaunimo įgyvendinamus kultūrines ir / ar pilietines veiklas.</p>
            <p>Gali skirti stipendijas už kultūrinę ir visuomeninę veiklą studentams</p>
        </section>

        <section id="socialines-apsaugos-ir-darbo-ministerija">
            <h4>Socialinės apsaugos ir darbo ministerija</h4>
            <p>Rūpinasi socialinės paramos skyrimo ir paskirstymo politika, kriterijais, pagal kuriuos nustatoma teisė
                gauti socialinę paramą (kriterijai dėl socialinių stipendijų). </p>
            <p>Rūpinasi sociailiai pažeidžiamų asmenų (pavyzdžiui, turinčių indivudalius poreikius) teisių apsaugojimo
                politika.</p>
            <p>Rūpinasi dirbančiųjų teisėmis ir darbo santykių prižiūrėjimu, darbuotojų, dirbančiųjų ir valstybės
                interesų derinimu.</p>
            <p>Plėtoja ir vysto nevyriausybinių (ir jaunimo) organizacijų veiklą.</p>
            <p>Sprendžia vyrų ir moterų lygybės klausimus, valstybės ir viešųjų institucijų įgyvendinamą politiką.</p>
            <p>Gali suteikti praktikas studentams, kurie studijuoja su socialiniu darbu ir politika susijusiąs studijų
                programas.</p>
            <p>Gali plėtoti socialinės paramos ir stipendijų rūšys, teikiamos paramos kriterijų išplėtimą, su studentų
                darbų susijusių problemų sprendimą.</p>
        </section>

        <section id="susisiekimo-ministerija">
            <h4>Susisiekimo ministerija</h4>
            <p>Atsako už keleivių vežimą, saugą ir nuolaidas geležinkelių, kelių, jūrų, vidaus vandenų, oro transporto
                srityje formuoja šiose sričių praktikas ir stipendijas.</p>
        </section>

        <section id="sveikatos-apsaugos-ministerija">
            <h4>Sveikatos apsaugos ministerija</h4>
            <p>Ministerija rūpinasi gyventojų sveikatos stiprinimu ir saugojimu, jog būtų užtikrinta kokybiška, saugi ir
                visiems vienodai prieinama sveikatos priežiūra bei ligų prevencija. Atsako už rezidentūros vietų
                valstybinio užsakymo suformavimą, rūpinasi pschikos sveikatos klausimais, sveikatos draudimo klausimu,
                e-sveikatos priežiūra, rūpinasi medicinos įstaigomis, rūpinasi skatinimo priemonėmis studentams,
                studijuojantiems sveikatos mokslų krypties programose.</p>
        </section>

        <section id="lrv-smsm">
            <h4>Švietimo, mokslo ir sporto ministerija</h4>
            <p>Atsako už švietimo klausimus, stojimą į aukštąsias mokyklas, pedagogikos studijas ir jų vystymą, aukštųjų
                mokyklų finansavimo, studijų vietų skirstymo studijų kokybės, socialinės dimensijos, fizinės sveikatos
                ir profesionalaus sporto klausimus. Rūpinasi skatinimo priemonėmis studentams, studijuojantiems
                susijusiose švietimo mokslų kryptyse. </p>

            <p><strong>Išsamiau į ŠMSM struktūrą nuoroda</strong>.</p>
        </section>

        <section id="teisingumo-ministerija">
            <h4>Teisingumo ministerija</h4>
            <p>Koordinuoja teisės klausimus, rūpinasi skatinimo priemonėmis studentams, studijuojantiems susijusių
                mokslų krypties programose.</p>
        </section>

        <section id="uzsienio-reikalu-ministerija">
            <h4>Užsienio reikalų ministerija</h4>
            <p>Koordinuoja ES klausimus, iš užsienio atvykusių studentų klausimų koordinavimą, mobilumo klausimus,
                rūpinasi skatinimo priemonėmis studentams, studijuojantiems susijusių mokslų krypties programose.</p>
        </section>

        <section id="vidaus-reikalu-ministerija">
            <h4>Vidaus reikalų ministerija</h4>
            <p>Rūpinasi regionų plėtros klausimais, NVO koordinavimu, studentų migracijos, viešojo saugumo klausimais,
                koordinuoja skatinimo priemones studentams, studijuojantiems susijusių mokslų krypties programose.</p>
        </section>

        <section id="zemes-ukio-ministerija">
            <h4>Žemės ūkio ministerija</h4>
            <p>Rūpinasi žemės ūkio mokslų vystymu, rūpinasi skatinimo priemonėmis studentams, studijuojantiems susijusių
                mokslų krypties programose.</p>
        </section>

    </section>

