            dėl atstovo delegavimo). Be konkrečių komisijų ar darbo grupių atvejų, nacionaliniu mastu atstovaujančios
            studentų savivaldos gali palaikyti dialogą su ŠMSM ministru, teikti ministerijai savo suformuotus pasiūlymus
            švietimo politikai.</p>
