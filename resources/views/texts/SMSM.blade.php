            įgyvendinimo koordinavimo Lietuvoje mes galime pamatyti du studentų atstovus (vienas iš VU SA, kitas iš LSS) iš
            17 narių. Studentų atstovai į suformuotas darbo grupes gali būti deleguojami proaktyviai (kai studentų savivalda
