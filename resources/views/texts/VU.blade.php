        <h3>VU schema</h3>
        <h4>Schemos aprašas</h4>
        <p>Vilniaus universiteto struktūra sudaryta remiantis Vilniaus universiteto Statutu ir kitais vidiniais veiklos
            dokumentais ir teisės aktais, jie pasiekiami <a
                href="https://www.vu.lt/apiemus/dokumentai#strateginiai-veiklos-planai">čia</a>.</p>

        <p>Vilniaus universitetas – seniausia šalies nacionalinės reikšmės mokslo ir studijų institucija. Universitetas
            turi Lietuvos Respublikos Konstitucijos, Lietuvos Respublikos mokslo ir studijų įstatymo ir savo Statuto
            laiduojamą autonomiją, visais galimais teisėtais būdais ir priemonėmis užtikrina, saugo ir gina kultūros,
            mokslo, tyrinėjimų ir dėstymo laisvę. </p>

        <p>Norėdami suprasti, kaip veikia aukštojo mokslo institucija, turime suvokti jos struktūrą, valdymo organus, jų
            funkcijas ir sąsajas, todėl šioje pateiktoje schemoje pateikiamos naudingos nuorodos į pagrindinius
            informacijos šaltinius, dokumentus, kurie atnaujinami jiems pasikeitus.</p>

        <p>Vilniaus universitetas savo Statute įtvirtina svarbias nuostatas visiems Vilniaus universiteto Studentams.
            Statutas suteikia svarbią teisę ir atsakomybę Vilniaus universiteto Studentų atstovybei atstovauti visiems
            Vilniaus universiteto studentams. Plačiau apie tai VU Statuto 21 straipsnio 8-12 dalyse.</p>

        <p>Šis aprašas ir schema leis lengviau atstovauti ir susivokti visoje sistemoje. Jei kiltų papildomų klausimų,
            kreipkitės į VU SA. </p>

        <p>Apie šiuos darinius plačiau galite susipažinti VU Statute.</p>
    </section>


    <section id="VU-organai">
        <p><strong>Rektorius</strong> – vienasmenis Universiteto valdymo organas ir Universiteto vadovas. Rektorius veikia Universiteto vardu ir jam atstovauja, atsako už Universiteto veiklos tikslų įgyvendinimą, kokybišką darbų organizavimą, Universiteto finansinę veiklą, tinkamą lėšų ir turto valdymą. </p> 

<p><a href="https://www.vu.lt/apiemus/struktura/rektorius">Plačiau apie Rektoriaus funkcijas, darbotvarkę.</a></p> 

<p><strong>Taryba</strong> – Universiteto valdymo organas atsakingas už strateginę Universiteto plėtrą, veiklos kokybę, finansus bei struktūros pokyčius.</p> 

<p>Tarybą sudaro vienuolika narių, iš kurių du yra VU SA deleguoti atstovai. <a href="https://www.vu.lt/apiemus/struktura/taryba/tarybos-sprendimai">Plačiau apie tarybos priimtus sprendimus.</a></p>

<p><strong>Senatas</strong> – užtikrina kultūros, mokslo, tyrimų ir dėstymo laisvę Universitete, Universiteto bendrųjų akademinių reikalų ir kitų tiesiogiai su viso Universiteto akademine veikla susijusių klausimų kolegialų aptarimą bei jiems įgyvendinti reikalingų teisės aktų priėmimą.</p> 

<p>Universiteto Senatą sudaro 41 narys, iš kurių 8 yra VU SA deleguoti atstovai.</p>  

<p><a href="https://www.vu.lt/apiemus/struktura/senatas/senato-sprendimai">Plačiau apie Senato priimtus sprendimus.</a></p>   

<p>Senato komitetai – itin svarbiems su mokslu, menu ir studijomis susijusiems Universiteto veiklos klausimams spręsti yra sudaryti trys Senato komitetai: Mokslo, Studijų bei Kokybės ir plėtros. Kiekvienas komitetas nagrinėja atitinkamos srities klausimus bei teikia išvadas Senatui. </p>

Šiuo metu Senate veikia 3 nuolatiniai komitetai: 

<ul>
    <li>Studijų komitetas – nagrinėja klausimus, susijusius su studijų vykdymu ir kokybe Vilniaus universitete.</li>  
    
    <li>Mokslo komitetas – nagrinėja klausimus, susijusius su mokslo vykdymu bei plėtra Vilniaus universitete.</li>  
    
    <li>Kokybės ir plėtros komitetas – kontroliuoja, kaip veikia studijų kokybės užtikrinimo sistema, nagrinėja su Vilniaus universiteto vidaus išteklių paskirstymu, plėtra susijusius klausimus.</li>
</ul> 

<p>Senato kolegija – Senato veiklai organizuoti sudaroma Senato kolegija, susidedanti iš Senato pirmininko, Senato pirmininko pavaduotojo (-ų) ir Komitetų pirmininkų ir Studentų atstovo. Senato kolegijos posėdžiuose Komitetų pirmininkai pristato Komitetuose apsvarstytus ir numatytus teikti Senatui klausimus bei tvirtinamas Senato metų darbo planas.</p>  

<p><a href="https://www.vu.lt/apiemus/struktura/senatas/senato-reglamentas">Plačiau apie Senato kolegiją ir Senato veiklą.</a></p>   

<h4>Senato komisijos</h4>

<p>Senato komisijos yra Senato sudarytos komisijos, padedančios įgyvendinti Universiteto tikslus bei uždavinius. Senato komisijos atskaitingos Senatui.</p> 

<p>Šiuo metu yra 7 Senato komisijos:</p> 

<ul>
    <li>Centrinė rinkimų komisija – kuria VU valdymo organų rinkimų tvarką ir užtikrina skaidrius bei tinkamus VU valdymo organų rinkimus. Veikloje vadovaujasi: <a href="https://www.vu.lt/site_files/VUCentrinesrinkimukomisijosnuostatai.pdf">https://www.vu.lt/site_files/VUCentrinesrinkimukomisijosnuostatai.pdf</a> </li> 
    
    <li>Centrinė akademinės etikos komisija – sprendžia ginčus bei klausimus, susijusius su akademinės etikos pažeidimais, kuria ir nuolatos atnaujina VU akademinės etikos kodeksą. Veikloje vadovaujasi: <a href="https://www.vu.lt/apiemus/dokumentai#akademine-etika">https://www.vu.lt/apiemus/dokumentai#akademine-etika</a></li>  
    
    <li>Centrinė ginčų nagrinėjimo komisija – nagrinėja skundus bei pareiškimus tarp Universiteto bendruomenės narių, nesusijusius su akademinės etikos pažeidimais. Veikloje vadovaujasi: <a href="https://www.vu.lt/apiemus/dokumentai#akademine-etika">https://www.vu.lt/apiemus/dokumentai#akademine-etika</a></li>  
    
    <li>Akademinio protokolo komisija – kuria ir nuolatos atnaujina akademinių apeigų protokolą bei akademinių renginių aprašus. Veikloje vadovaujasi: <a href="https://www.vu.lt/site_files/Akademiniu_apeigu_protokolas.pdf">https://www.vu.lt/site_files/Akademiniu_apeigu_protokolas.pdf</a></li>  
    
    <li>Emeritūros, afiliacijos ir garbės vardų komisija – svarsto ir teikia Senatui tvirtinti kandidatūras gauti Universiteto profesoriaus emerito, afiliuotojo profesoriaus, docento, mokslo darbuotojo bei garbės daktaro, alumni, mecenato vardus. Veikloje vadovaujasi: <a href="https://www.vu.lt/site_files/Emerituros_afiliacijos_ir_garbes_vardu_nuostatai.pdf">https://www.vu.lt/site_files/Emerituros_afiliacijos_ir_garbes_vardu_nuostatai.pdf</a></li> 
    
    <li>Bendrųjų universitetinių studijų komisija – nagrinėja naujai teikiamų bendrųjų universitetinių studijų modulius, teikia išvadas dėl jų atitikimo bendrųjų universitetinių studijų koncepcijai, kuri vėliau tvirtinimui yra teikiama  Senatui ir atlieka šių modulių įgyvendinimo kokybės stebėseną. Veikloje vadovaujasi: <a href="https://www.vu.lt/site_files/BUS_koncepcija_2018-01-23_redakcija.pdf">https://www.vu.lt/site_files/BUS_koncepcija_2018-01-23_redakcija.pdf</a></li>
</ul>  

<h4>Rektoriaus komanda (Mažasis rektoratas)</h4> 

<p><strong>Vicerektorius/ė</strong> - Rektoriaus teikimu vieną iš prorektorių Taryba skiria eiti pirmojo prorektoriaus – vicerektoriaus – pareigas.</p>  

<p>Prorektoriai –  administruoja Rektoriaus jiems pavestą Universiteto veiklos ar valdymo sritį. Kiekvienas jų atitinkamoje srityje veikia Rektoriaus vardu ir padeda valdyti Universitetą. Šiuo metu Universitete yra 4 prorektoriai:</p> 

<ul>
    <li>Mokslo prorektorė; </li>
    
    <li>Studijų prorektorius;</li>  
    
    <li>Partnerystės prorektorius;</li>  
    
    <li>Organizacijos vystymo ir bendruomenės reikalų prorektorius.</li>
</ul> 

<h4>Kancleris</h4> 

<p>Kancleris padeda Rektoriui valdyti Universiteto išteklius, tvarkyti finansus, užtikrinti Universiteto vidaus tvarką ir reikiamas materialines bei organizacines sąlygas Universiteto Senato ir Tarybos veiklai.</p>  

<p><a href="https://www.vu.lt/apiemus/struktura/vicerektore-prorektoriai-ir-kancleris">Plačiau apie Rektoriaus komandos narius.</a></p>  

<p>Patarėjai - Rektorius atsižvelgdamas į poreikį gali skirti patarėjus įvairiais klausimais.</p> 

