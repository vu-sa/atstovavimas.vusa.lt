<h4>Rektoriaus sudaromos komisijos ir komitetai</h4> 

<p>Rektorius gali formuoti komisijas ir komitetus svarbiems Universiteto klausimams spręsti.</p>  

<p>Partnerystės profesorių ir partnerystės docentų pareigų suteikimo patariamasis komitetas – Komitetas VU rektoriui teikia nuomonę dėl VU partnerystės profesorių ir partnerystės docentų pareigybių suteikimo. Šios pareigybės leidžia paskaitas Vilniaus universitete dėstyti garsiems Lietuvos verslo atstovams, diplomatams ir kitiems savo sričių specialistams, užimantiems vadovaujančias pareigas ar turintiems didžiulę praktinio darbo patirtį. Komitetas savo veikloje vadovaujasi šiais nuostatais: <a href="https://www.vu.lt/site_files/Reguliaminas/Dėl_partnerystės_profesoriaus_ir_partnerystės_docento_pareigybių.pdf">https://www.vu.lt/site_files/Reguliaminas/Dėl_partnerystės_profesoriaus_ir_partnerystės_docento_pareigybių.pdf</a></p>

<p>VU geriausių dėstytojų pripažinimo komisija – Komisija svarsto kandidatų veiklą ir teikia Rektoriui rekomendacijas dėl kandidatų pripažinimo geriausiais metų dėstytojais. Kandidatūras iš Vilniaus universiteto studentų siūlomų dėstytojų Komisijos svarstymui teikia kamieninio akademinio padalinio administracija kartu su VU SA padaliniu. Komisija savo veikloje vadovaujasi šiuo aprašu: <a href="https://www.vu.lt/site_files/Isak_VU_geriausiu_destytoju_rinkimo_aprasas_1.pdf">https://www.vu.lt/site_files/Isak_VU_geriausiu_destytoju_rinkimo_aprasas_1.pdf</a></p>  

<p>VU priėmimo komisija – Komisija svarsto ir teikia Universiteto rektoriui tvirtinti dėstytojų ir mokslo darbuotojų kandidatūras akademinėms pareigoms užimti.</p>  

<p>Plačiau apie procedūrą: <a href="https://www.vu.lt/apiemus/karjera/akademinis-personalas/6546-konkursu-organizavimo-nuostatai-ir-kiti-susije-dokumentai">https://www.vu.lt/apiemus/karjera/akademinis-personalas/6546-konkursu-organizavimo-nuostatai-ir-kiti-susije-dokumentai</A></p>  

