<h4>Rektoriaus sudaromos darbo grupės</h4> 

<p>Rektorius taip pat gali steigti  darbo grupes tam tikriems klausimams spręsti. Darbo grupės yra trumpalaikės ir sudarytos iki reikiamo sprendimo priėmimo (pavyzdžiui, Statuto parengimo, modulinių studijų įvedimo ir kt.). </p> 

<p>Didysis Rektoratas – Rektoriaus patariamasis darinys, sudarytas iš visų Universiteto kamieninių akademinių ir neakademinių padalinių vadovų bei Studentų atstovybės prezidento. Rektoratas svarsto visus svarbiausius su mokslu, studijomis, finansais ir plėtra susijusius klausimus bei teikia išvadas Universiteto Tarybai, Senatui, Rektoriui, kurie pagal savo veiklos sritį priima galutinius sprendimus.</p>  

<p><a href="https://www.vu.lt/apiemus/struktura/rektorato-sudetis">Plačiau apie Didžiojo rektorato sudėtį.</a></p>  

<p>Vilniaus universiteto kamieniniai akademiniai padaliniai (toliau – VU KAP) – fakultetai, institutai, centrai, mokyklos – tai vidiniai organizaciniai Universiteto dariniai, kuriuose vykdoma visų rūšių pagrindinė Universiteto veikla.</p>  

<p>Veikla grindžiama pavyzdiniais <a href="https://www.vu.lt/site_files/Senatas_Taryba/S-2016-2/KAP_pavyzdiniai_nuostatai_PATVIRTINTI_po_redagavimo.pdf">VU KAP nuostatais</a>.</p>  

<p>Šiuo metu Universitete yra <a href="https://www.vu.lt/apiemus/struktura/padaliniai">15 VU KAP</a>.</p>  

<p>Išsamiau apie VU KAP - SUDĖT LINKĄ Į STRUKTŪRĄ KUR BUS PUSLAPYJE, KAD PASPAUDUS KONKREČIAI TAVE Į TĄ VIETĄ NUMESTŲ AUTOMATIŠKAI ( KUR EFKA SCHEMA YR PADARĘS)</p>

<p>Vilniaus universiteto kamieniniai neakademiniai padaliniai (toliau – VU KNAP). Jie steigiami paslaugoms, užtikrinančioms akademinių padalinių veiklą, teikti ir kitoms neakademinėms Universiteto funkcijoms atlikti. Šių organizacinių darinių vieta Universiteto valdymo struktūroje nustatyta Pavyzdiniuose Vilniaus universiteto kamieninių neakademinių padalinių nuostatuose, kuriuos tvirtina Rektorius.</p> 

<p>Visi VU KNAP pateikiami <a href="https://www.vu.lt/apiemus/struktura/padaliniai">čia</a>.</p>   

<p>VU KNAP pavyzdiniai nuostatai: <a href="https://www.vu.lt/site_files/Reguliaminas/Pavyzdiniai_VU_kamieninio_neakademinio_padalinio_nuostatai_2019-10-16.pdf">https://www.vu.lt/site_files/Reguliaminas/Pavyzdiniai_VU_kamieninio_neakademinio_padalinio_nuostatai_2019-10-16.pdf</a></p>  

<p>Vilniaus universiteto Studentų atstovybė (VU SA) – seniausia ir didžiausia Lietuvoje visuomeninė, ne pelno siekianti, nepolitinė, ekspertinė švietimo organizacija, atstovaujanti studentų interesams Vilniaus universitete bei už jo ribų, puoselėjanti universitetinę kultūrą ir kurianti palankiausias sąlygas studentų saviraiškai.</p> 

<p>VU SA yra vienintelė studentų atstovybė, kuri savo studentų interesams atstovauja nacionaliniu mastu.</p>

<p>Studentas – asmuo, studijuojantis aukštojoje mokykloje pagal tam tikrą studijų programą arba doktorantūroje.</p>
