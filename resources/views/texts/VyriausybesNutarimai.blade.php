        <h2>VYRIAUSYBĖS NUTARIMO PRIĖMIMO PROCESAS</h2>
    </section>

    <p>Teisės aktų projektai registruojami Vyriausybės kanceliarijoje. </p>
    <p>
        <strong>
            O kaip užregistruoti teisės aktą?
            Tai galima padaryti keliais būdais:
        </strong>
    </p>
    <ol>
        <li><p>Vyriausybei įstatymų, nutarimų ir kitų teisės aktų projektus turi teisę teikti Ministras Pirmininkas,
                ministrai, savivaldybių tarybos ir socialiniai partneriai (asociacijos, profesinės sąjungos,
                nevyriausybinės organizacijos).</p>
            <p>Svarbu yra tai, jog Ministro Pirmininko teikiami teisės aktų projektai turi būti pasirašyti Ministro
                Pirmininko, ministro teikiami teisės aktų projektai – ministro. </p>

            <p>O pavyzdžiui, savivaldybių tarybų ir socialinių partnerių teikiami teisės aktų projektai turi būti
                pasirašyti atitinkamos srities ministro. Vyriausybei teikiamas teisės akto projektas, kurį parengė ir
                teikė tam tikra institucija, turi būti suderintas su kitomis institucijomis pagal nustatytą Vyriausybės
                darbo reglamento tvarką.</p>
        </li>

        <li><p>Teisės aktai gali būti pateikiami ir kaip asmenų prašymai ir skundai bei piliečių peticijos. </p>
            <p>Vyriausybei ar Ministrui Pirmininkui adresuoti asmenų prašymai ir skundai Vyriausybės kanclerio
                persiunčiami atsakingoms institucijoms ir apie tai pranešama prašymą ar skundą pateikusiam asmeniui.
            </p>
            <p>O Vyriausybės Peticijų komisijos pateiktos išvados dėl peticijoje išdėstytų reikalavimų ir pasiūlymų
                (ne)tenkinimo registruojamos Vyriausybės kanceliarijoje. Tada išvados perduodamos atitinkamoms
                institucijoms, jog būtų parengtas atitinkamas teisės aktas dėl peticijos reikalavimų įgyvendinimo.</p>
        </li>
    </ol>

    <p>Tada į teisėkūros procesą, pateikdama savo pasiūlymus, gali įsitraukti ir visuomenė. </p>

    <p>Su visuomene paprastai konsultuojamasi iniciatyvos pradžios stadijoje, sudarant sąlygas suinteresuotiems asmenims
        (fiziniams asmenims, asociacijoms, nevyriausybinėms organizacijoms, profesinėms sąjungoms, valstybės ir
        savivaldybių institucijoms ir įstaigoms), pateikti pastabas ir pasiūlymus teisės aktų projektams. </p>

    <p>
        Dėl teisės akto projektui teikiamų pasiūlymų turi būti pateiktas įvertinimas, kiek realu tai įgyvendinti, ir
        išvados iš Vyriausybės kanceliarijos. Kanceliarija išvadas rengia konsultuodamasi su atsakingomis
        ministerijomis. Pasiūlymai ir pastabos teisės akto projektui suderinamos su projekto rengėjais ir teikiamos
        Vyriausybės kancleriui siūlyti įtraukti į Vyriausybės posėdžio darbotvarkės projektą.
    </p>

    <p>Teisės akto projektą rengianti institucija, Vyriausybės komisija turi užtikrinti tinkamą teikiamo teisės akto
        projekto parengimą, konsultavimąsi su visuomene, nustatytų terminų laikymąsi. Kartu su teisės akto projektu
        rengėjas teikia ir iš suinteresuotų institucijų gautų pastabų ir pasiūlymų įvertinimą.</p>
    <p>Projektas, patikslintas pagal Vyriausybės kanceliarijos padalinių išvadose pateiktas pastabas ir pasiūlymus,
        teikiamas Vyriausybei. Ministras Pirmininkas gali siūlyti svarstyti teisės akto projektą atitinkamame
        Vyriausybės komitete arba Vyriausybės pasitarime.</p>

    <section id="vyriausybes-pasitarimai-ir-posedziai">
        <h3>Vyriausybės pasitarimai ir posėdžiai</h3>
        <p>Vyriausybės kancleris teikia Ministrui Pirmininkui pasiūlymus dėl Vyriausybės pasitarimo ir posėdžio
            darbotvarkių sudarymo. Ministras Pirmininkas nustato Vyriausybės pasitarimo ir Vyriausybės posėdžio datą ir
            laiką. Vyriausybės pasitarimams ir Vyriausybės posėdžiams pirmininkauja Ministras pirmininkas. Vyriausybės
            pasitarimuose ir Vyriausybės posėdžiuose dalyvauja Vyriausybės nariai.</p>

        <section id="vyriausybes-pasitarimas">
            <h4>Vyriausybės pasitarimas</h4>
            <p>Pasitarimai vyksta prieš Vyriausybės posėdžius, kuriuose aptariami svarbiausi klausimai. Vyriausybės
                pasitarimuose svarstomi ir priimami Vyriausybės sprendimai ir rezoliucijos.</p>
        </section>
        <section id="vyriausybes-posedis">
            <h4>Vyriausybės posėdis</h4>
            <p>Vyriausybės pasitarime suderinti klausimai svarstomi Vyriausybės posėdyje. Vyriausybės nutarimai priimami
                Vyriausybės narių balsų dauguma.</p>
        </section>
        <section id="vyriausybes-nutarimai">
            <h4>Vyriausybės nutarimai</h4>
            <p>Vyriausybės nutarimai perduodami atsakingoms institucijoms: Seimui, Respublikos prezidentui.</p>
        </section>
