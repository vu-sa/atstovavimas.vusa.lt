        <section id="situacijos-analize">
            <h4>2.1. Situacijos analizė ir problemos identifikavimas</h4>
            <p>Pirmiausia atliekama situacijos analizė – identifikuojama jau esama teisinė bazė, bandoma surasti
                naudingos informacijos, bendraujama su studentų atstovais ir suinteresuotosiomis šalimis, kurios yra
                susijusios su inicijuojamu pakeitimu. Atlikus šią analizę matoma visa reali situacija ir tada
                pasirenkama konkreti problema, kurią norima spręsti. Surenkama visa svarbiausia su pasirinkta problema
                susijusi informacija (statistika, nuorodos į aktualius dokumentus ir pan.). Ši medžiaga susisteminama
                bei panaudojama keliantis tikslą ir sudarinėjant problemos planą. Šiame etape svarbu išsikelti
                potencialius klausimus, kurie reikalingi tyrimo atlikimui. </p>
        </section>
        <section id="tikslas">
            <h4>2.2. Tikslas</h4>
            <p>Identifikavus problemą, atstovas turi keliauti prie tikslo išsikėlimo, kuris yra sąlygojamas
                suinteresuotųjų šalių. Šiame etape galima vadovautis SMART principu, kuris formuluojamam tikslui kelia
                šiuos reikalavimus, t.y., nurodo, koks turėtų būti tikslas:</p>
            <ul>
                <li>Konkretus – formuluotė aiški bei suprantama, yra akivaizdu, ko norima konkrečiai pasiekti;</li>
                <li>Išmatuojamas – aiškiai įvardinta, kokius rodiklius ar kriterijus matuosime po numatyto termino.
                    Kitaip tariant, turi būti aišku, kada tikslas bus pasiektas ne tik laiko, bet ir rezultato
                    atžvilgiu;</li>
                <li>Įgyvendinamas – realus rezultato pasiekiamumas, t.y., realu pasiekti išsikeltą tikslą;</li>
                <li>Prasmingas – siekiama aktualaus, esmingo pokyčio;</li>
                <li>Apibrėžtas laike – aiškiai apibrėžta tikslo įgyvendinimo pradžia ir pabaiga.</li>
            </ul>
            <p>Tikslas turi būti tik <strong>vienas</strong>. Rekomenduojama tikslą formuluoti taip, kad jis būtų kuo
                mažiau kintantis.</p>
        </section>
