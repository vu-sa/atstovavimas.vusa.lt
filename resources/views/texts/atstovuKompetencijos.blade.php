<section id="atstovu-kompetencijos">
    <h2>Studentų atstovo veikla ir kompetencijos</h2>

    <p>Studentų atstovo veiklą galime apibrėžti skirtingai instituciniu ir nacionaliniu lygmenimis. Tačiau,
        neatsižvelgiant į šias skirtingas dedamąsias, atstovavimą mes galime suprasti kaip savotiškus susitarimus,
        neretai atsispindinčius pareigybių aprašuose (jeigu tokie sudaromi), kurie priklauso nuo studentų savivaldos
        veiklos, turimų išteklių, dabarties aktualijų ir kitų kintamųjų. Tuo tarpu atstovavimui reikalingos
        kompetencijos yra iškeliamos iš vykdomos veiklos ir ugdomos studentų savivaldos organizuojamų mokymų eigoje,
        atstovui vykdant savišvietą arba mokantis iš patirties. </p>
    <p>Toliau šiame gide yra aprašoma problemos sprendimo eiga.</p>
    <section id="problemos-sprendimo-eiga">
        <h3>1. Problemos sprendimo eiga</h3>
        <p>Šiame skyriuje yra pristatomas problemos sprendimo procesas nuo pat jo susiplanavimo iki atsiskaitymo
            studentams už atliktą veiklą remiantis įprastine studentų savivaldų patirtimi bei praktikomis. Toliau
            pateikiama medžiaga yra rekomendacinio pobūdžio ir ja reikėtų naudotis lanksčiai, kadangi nėra vieno
            teisingo kelio sprendžiant įvairias iškylančias situacijas.</p>
        <p>Problemos sprendimas gali būti gana ilgas ir sudėtingas procesas, reikalaujantis aiškaus tikslo, detalaus
            susiplanavimo, jau turimų bei papildomai surinktų duomenų analizės ir loginio mąstymo gebėjimų. </p>
        <p>Dažnu atveju, remiantis gerosiomis užsienio patirtimis, šis procesas vyksta koordinuotai – studentų atstovai
            visuose žingsniuose bendradarbiauja su jų veiklą koordinuojančiu asmeniu, kuris konsultuoja atstovavimo
            klausimais, teikia reikiamą metodinę medžiagą, pagal poreikį organizuoja kompetencijų kėlimo užsiėmimus ir
            stebi bei registruoja studentų atstovų veiklą siekiant efektyvios vidinės komunikacijos.</p>
        <p>Itin svarbu pabrėžti, jog kiekvienas veiksmas problemų sprendimo procese nuo pat pirmojo turi būti atliktas
            kuo kokybiškiau, kadangi nepastebėtos klaidos gali smarkiai ir negrįžtamai paveikti tolimesnių veiksmų
            kokybę. Toliau yra pateikiama problemos sprendimo proceso eiga, o likusiuose poskyriuose atkreipiamas
            dėmesys į šiuos žingsnius:</p>
        <ol>
            <li> Problemos identifikavimas ir sprendimo kelio planavimas</li>
            <li>Duomenų rinkimas, analizavimas ir apipavidalinimas</li>
            <li>Pasiūlymo rengimas, nešimas ir atsiskaitymas studentams</li>
        </ol>
    </section>

