    <section id="duomenys">
        <h3>3. Duomenų rinkimas, analizavimas ir apipavidalinimas</h3>
        <p>Nagrinėjant problemą mes naudojame informaciją ir duomenis. Pirminei problemos analizei pasitelkiame
            antrinius duomenis iš jau esamų šaltinių (pavyzdžiui, STRATA, „Eurostudent“, „Eurydice“ ir t.t.), o esant
            poreikiui gilesnei problemos analizei inicijuojame pirminių duomenų rinkimą.</p>

        <p>Studentų nuomonės rinkimo iniciatyvumas gali būti dvejopas – nuomonė gali būti renkama reaktyviai (kai to
            reikalauja išorinės paskatos) arba proaktyviai (nepaisant išorinių paskatų siekiant patikrinti problemų
            (ne)buvimą tam tikru klausimu). Yra įvairių kiekybinių bei kokybinių nuomonės rinkimo formatų (stebėjimas,
            turinio analizė ir t.t.), bet dažniausi studentų atstovavimo kontekste yra naudojami šie trys: focus grupė,
            anketavimas arba mišrusis būdas (focus grupės ir anketavimo derinys).</p>
        <p>
            Focus grupė – tai yra fizinis (arba virtualus) susitikimas su studentais, kurį moderuoja studentų atstovas
            ir kuris yra protokoluojamas siekiant pažymėti dalyvių išsakytas mintis. Šis formatas yra naudingas tuo, jog
            juo įmanoma užtikrinti daugiau kokybinių duomenų, kadangi moderuojant pokalbį galima pateikti patikslinamųjų
            klausimų, palaikyti diskusiją su focus grupės dalyviais. Vis dėlto, kadangi įprastai focus grupėse dalyvauja
            mažai studentų (efektyviausia, kai dalyvauja 6–10 studentų), naudojant tik šį formatą negaunama daug
            kiekybinių duomenų, nebent yra organizuojamos kelios focus grupės tuo pačiu klausimu su skirtingais
            dalyviais.
        </p>

        <p>Kitas studentų nuomonės rinkimo formatas yra anketavimas (elektroninės arba fizinės apklausos). Šis formatas
            gali būti naudingas tuo, jog apklausa galima gauti daugiau kiekybinių duomenų, ir taip pat yra sukuriamos
            galimybės visai tikslinei auditorijai įsitraukti į nuomonės rinkimo procesą. Kita vertus, didžiausia grėsmė
            vykdant apklausas (ypač su atvirais klausimais) yra trumpi, nereprezentatyvūs duomenys, kurių nebeįmanoma
            patikslinti. Siekiant išvengti šios grėsmės yra būtina skirti pakankamai dėmesio apklausos klausimų
            formulavimui, jų skaičiaus nustatymui, o taip pat pagal poreikį derinti šį formatą su focus grupės formatu.
        </p>

        <p>Surinkus duomenis galima pereiti prie jų analizavimo ir apipavidalinimo. Šie du veiksmai yra tiek ir susiję
            tarpusavyje, tiek ir priklausomi nuo taikyto duomenų rinkimo metodo. Be to, kadangi neretai gautų duomenų
            yra nemažai, analizuojant ir apipavidalinant duomenys yra pasitelkiama įvairi programinė įranga – SPSS,
            „Microsoft Excel“ ir t.t. Pirmasis žingsnis analizuojant duomenis būtų jų paruošimas apdorojimui – taikius
            kiekybinius metodus šį etapą apima duomenų „valymas“ ir filtravimas, o taikius kokybinius metodus yra
            transkribuojami įrašai (jeigu buvo įrašytas garsas) ir išplečiami užrašai. Pavyzdžiui, paprastoje
            kiekybinėje analizėje dažniausiai atkreipiamas dėmesys į tokius dydžius, kaip vidurkis, mediana ar moda, o
            duomenys būna apipavidalinami procentinėmis išraiškomis bei grafinio pavaizdavimo priemonėmis (diagramos,
            lentelės). Taikant kiekybinius duomenų analizavimo metodus duomenų koreliacija nebūtinai parodo jų
            priežastinį ryšį.</p>

        <p>Analizuojant kokybinius duomenis, didžiausią dėmesį reikėtų skirti pasikartojantiems šablonams, t.y. siekiama
            atrasti pasikartojančias sąveikas, nuomonę ir pan., kurie būna išreiškiami įvairiais žodžiais ar sakiniais,
            bet turi tą pačią pagrindinę mintį. Kokybinės duomenų analizė skirstoma į turinio ir diskurso analizę.
            Turinio analizėje duomenys yra skirstomi į kategorijas pagal panašius pasisakymus arba temas žodžių, frazių
            arba pastraipų lygmeniu. Diskurso analizėje yra akcentuojama išraiškų prasmė – teigiama, kad net ir nedideli
            frazių skirtumai gali pakeisti prasmę, tad duomenys yra tiriami kur kas smulkiau.</p>

        <p>Kad ir koks duomenų analizavimo metodas būtų pasirinktas, apipavidalinti duomenys (kuriuos jau galėtume
            vadinti informacija) privalo būti pagrindžiami, detalūs, nuasmeninti, lengvai suprantami. Kadangi visa
            suformuota informacija arba atitinkamos jos dalys bus integruotos į rengiamą pasiūlymą, ją galima išsaugoti
            lengvai atidaromu failo formatu. Rekomenduojama šiame etape parengtą informaciją pateikti kitų studentų
            atstovų arba koordinuojančių asmenų peržiūrai, gauti iš jų grįžtamąjį ryšį ir pagal jį patobulinti parengtą
            medžiagą. Taip pat svarbu yra ją įkelti į studentų savivaldos administruojamą archyvą arba kitas duomenų
            talpyklas siekiant užtikrinti institucinę atmintį.</p>
    </section>

