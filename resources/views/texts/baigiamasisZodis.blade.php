    <h2>Baigiamasis žodis</h2>
    
    <p><strong>Metodinis leidinys: „Atstovavimo gidas”, Vilnius: Vilniaus universiteto Studentų atstovybė, 2021.</strong> </p>

    <p>Metodiniame leidinyje „Atstovavimo gidas“ pateikta informacija susideda iš dviejų dalių. Pirmojoje –aprašoma ir atvaizduojama aukštojo mokslo politikos ir su ja susijusių struktūrų schemos, kurios padės studentų atstovui geriau susigaudyti, į kurias iš šalių tikslingiausia kreiptis arba kokius klausimus nagrinėti. Taip pat pristatomas įstatymų iniciavimo ir Vyriausybės nutarimų priėmimo keliai. Antrojoje – susisteminta praktinė informacija, kuri reikalinga studentų atstovams veikiant nacionalinėje erdvėje. Šis interaktyvus elektroninis leidinys bus atnaujinamas aktualiausia praktine informacija studentus atstovaujančioms organizacijoms, studentų atstovams ir aukštuoju mokslu besidomintiems asmenims. Leidinys gali būti panaudojamas kaip mokomojo tipo priemonė. </p>
