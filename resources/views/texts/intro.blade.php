    <h2>Įvadas</h2>
    <p>Prieš Jūsų akis – VU SA sukurtas atstovavimo gidas. Jo tikslas – supažindinti studentų atstovus ir atstovavimu
        besidominčius asmenis su nacionaline aukštojo mokslo politika ir jos dalyviais. Šiuo gidu siekiame NVO atstovų
        kompetencijų reikalingų pasiūlymams parengti ir pristatyti stiprinimo.</p>
    <p>VU SA, siekdama paskatinti piliečius aktyviau įsitraukti į viešąjį valdymą švietimo srityje, nuo 2018 m. vykdo
        projektą „Viešojo valdymo švietimo srityje gerinimas, įtraukiant nevyriausybines organizacijas“. Projektas
        finansuojamas iš Europos Sąjungos struktūrinių fondų lėšų.</p>
    <p>Įsitraukimas į švietimo politikos procesus prasideda nuo atstovavimo. Tam, kad galėtume tikslingai atstovauti,
        visų pirma, turime sau atsakyti į klausimą, ką mes laikome atstovavimu. Ši veikla gimė iš poreikio apginti tų
        žmonių interesus, kurie negalėdavo to padaryti dėl tam tikru laikotarpiu vyravusių teisinių ar kitokių
        apribojimų.</p>
    <p>Atstovavimą, kaip reiškinį, galima išskirti į šias sudedamąsias:</p>

    <ul>
        <li>Atstovaujama šalis (pavyzdžiui, studentai)</li>
        <li>Atstovas (asmuo, kuris vykdo atstovavimo veiklas)</li>
        <li>Tikslas (kokio pokyčio yra siekiama)</li>
        <li>Suinteresuotos šalys (įvairūs organai, institucijos ir panašiai, kurių sudėtyje veikia arba su kuriomis yra
            susijęs atstovas)</li>
        <li>Kompetencijos (žinios, įgūdžiai ir vertybinės nuostatos, įgalinančios atstovą atlikti savo funkcijas)</li>
        <li>Veikla (su atstovavimo procesu susijusios ir tam tikras taisykles turintys veiksmai, kaip antai problemos
            sprendimas)</li>
    </ul>
    <p>
        O dabar apie patį leidinį. Jame pasistengsime aptarti, kokios suinteresuotosios šalys dalyvauja formuojant ir
        įgyvendinant aukštojo mokslo politikos procesus.
        Leidinį sudaro dvi dalys: pirmoji dalis sudaryta iš aukštojo mokslo politikos dalyvių, o antrojoje dalyje galite
        rasti patarimus studentų atstovų veiklai ir kompetencijos kėlimui.
    </p>
    <p>Pirmojoje dalyje pavaizduotos keturios schemos. Pirmojoje atvaizduojami Mokslo ir studijų įstatyme nurodyti
        aukštojo mokslo politikos dalyviai. O kitose schemose nurodyti Seimo, Vyriausybės, Švietimo, mokslo ir sporto
        ministerijos ir joms pavaldžių institucijų veikla. Taip pat atvaizduojamas įstatymų ir Vyriausybės nutarimų
        leidybos ir priėmimo procesai.</p>
    <p>Schemose pasirinkus Jus dominančią organizaciją ar instituciją atsidarys trumpas jos veiklos aprašymas su aktyvia
        nuoroda į aktualią informaciją. Po schema – jos aprašymas, po kuriuo išdėstytos visų schemose atvaizduojamų
        organizacijų santraukos.</p>
    <p>Antrojoje dalyje informacija išdėstyta nuosekliai: kaip identifikuoti problemas ir planuoti jų sprendimą,
        susirinkti duomenis, juos analizuoti, rengti pasiūlymus, juos tikslingai perduoti suinteresuotosioms šalims ir
        atsiskaityti studentams.</p>
</section>

