    <h3> Regioninės jaunimo politikos aprašas </h3>

    <p> Teisę rinkti savivaldybės tarybos narius ir savivaldybės merą turi <strong> nuolatiniai šios
            savivaldybės gyventojai</strong>, kuriems rinkimų dieną yra sukakę 18 metų.
        Savivaldybių tarybų rinkimuose nedalyvauja asmenys, teismo pripažinti
        neveiksniais. </p>

    <h4>LR savivaldybių taryba</h4>

    <p> Lietuvos Respublikos savivaldybių tarybų (toliau – savivaldybių taryba, taryba) nariai
        renkami ketveriems metams, remiantis visuotine ir lygia rinkimų teise, slaptu balsavimu
        tiesioginiuose rinkimuose pagal mišrią rinkimų sistemą. Savivaldybės tarybą sudaro
        savivaldybės tarybos nariai. Vienas tos savivaldybės tarybos narys yra meras. </p>

    <h4>Savivaldybės administracijos direktorius</h4>

    <p> Savivaldybės administracijos direktorius į pareigas skiriamas mero teikimu savivaldybės
        tarybos sprendimu savivaldybės tarybos įgaliojimų laikui politinio (asmeninio) pasitikėjimo
        pagrindu.</p>
    <p> Savivaldybės administracijos direktorius vadovauja savivaldybės administracijai.
        Jis yra įstaigos vadovas. Savivaldybės administracijos direktoriaus skyrimo ir atleidimo
        tvarka nustatyta šiame ir Valstybės tarnybos įstatymuose. </p>

    <h4>Savivaldybės jaunimo reikalų taryba</h4>

    <p> Savivaldybės jaunimo reikalų taryba – visuomeninė patariamoji institucija, vadovaujantis
        lygybės principu sudaroma iš savivaldybės administracijos ir / ar savivaldybės tarybos ir
        savivaldybės teritorijoje veikiančių savivaldybės jaunimo organizacijų tarybos deleguotų
        atstovų. </p>

    <p> Savivaldybės jaunimo reikalų taryba nagrinėja su jaunimo politika savivaldybėje
        susijusius klausimus ir teikia savivaldybės institucijoms ir įstaigoms pasiūlymus dėl
        jaunimo politikos, jos įgyvendinimo, jaunimo politikos įgyvendinimo priemonių
        finansavimo prioritetų savivaldybėje, su jaunimo politikos įgyvendinimu savivaldybėje
        susijusių teisės aktų projektų.</p>

    <p> Jeigu savivaldybės jaunimo organizacijų tarybos nėra ar ji nedelegavo atstovų, jaunimo
        atstovai išrenkami viešame visuotiniame jaunimo organizacijų ir su jaunimu dirbančių
        organizacijų, mokinių ir (ar) studentų savivaldos atstovų, veikiančių savivaldybės
        teritorijoje, susirinkime. Už šio susirinkimo organizavimą yra atsakingas savivaldybės
        jaunimo reikalų koordinatorius. </p>

    <h4>Savivaldybės jaunimo reikalų koordinatorius</h4>

    <p> Savivaldybės jaunimo reikalų koordinatorius rengia ir įgyvendina savivaldybėje
        įgyvendinamas jaunimo politikos programas ir priemones, analizuoja jaunimo, jaunimo
        organizacijų ir su jaunimu dirbančių organizacijų padėtį savivaldybėje, plėtoja
        savivaldybės institucijų ir įstaigų, dirbančių jaunimo politikos srityje, bendradarbiavimą,
        palaiko ryšius su užsienio lietuvių jaunimo organizacijomis ir atlieka kitas savivaldybės
        jaunimo reikalų koordinatoriaus pareigybės aprašyme nustatytas funkcijas;</p>

    <h4>Savivaldybės jaunimo organizacijų taryba</h4>

    <p> Savivaldybės jaunimo organizacijų taryba – jaunimo organizacija, kurios pagrindinis
        veiklos tikslas – vienyti savivaldybės teritorijoje veikiančias jaunimo organizacijas ir joms
        atstovauti. </p>

