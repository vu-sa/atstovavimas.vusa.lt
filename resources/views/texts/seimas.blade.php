<h3>Seimo Valdyba</h3>

<p>Seimo valdybą sudaro Seimo Pirmininkas, jo pavaduotojai ir Seimo opozicijos lyderis. Valdyba sprendžia organizacinius Seimo darbo klausimus ir teikia patarimus Seimo Pirmininkui. Prireikus, Seimo valdyba nustato, kokioms institucijoms ar asmenims pagrindinis komitetas privalo nusiųsti įstatymo projektą išvadoms gauti. Taip pat valdyba gali sudaryti darbo grupes įstatymams parengti bei organizuoti bendrą komitetų darbą. </p>

<h3>Frakcijos </h3>

<p>Seimo nariai pagal politines partijas ar vertybines nuostatas gali jungtis į frakcijas sudarytas iš ne mažiau kaip 7 narių, kuriose siekia politinių tikslų įgyvendinimo. Seimo narių frakcijai vadovauja ir jai Seime atstovauja frakcijos seniūnas ar seniūno pavaduotojas. Seimo nariai, neįsiregistravę į frakcijas, pripažįstami vienos mišrios Seimo narių grupės, kuri turi frakcijos teises, nariais. </p>  

<h3>Seniūnų sueiga</h3>

<p>Seime veikia Seniūnų sueiga, į kurią įeina Seimo valdybos nariai ir frakcijų atstovai, kurių skaičius proporcingas frakcijų narių skaičiui. Pagrindinis Seniūnų sueigos uždavinys –svarstyti Seimo sesijos darbų programas bei posėdžių darbotvarkes, derinti Seimo komitetų ir frakcijų darbo organizavimo klausimus, teikti sprendimų šiais klausimais projektus Seimui bei valdybai, taip pat patarimus Seimo Pirmininkui. </p>

